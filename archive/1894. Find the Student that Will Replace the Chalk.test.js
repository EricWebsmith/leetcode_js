/**
 * 
 * @param {number[]} arr 
 * @param {number} v 
 * @returns {number}
 */
function binarySearch(arr, v) {
    const n = arr.length;
    let left = 0;
    let right = n - 1;
    let mid = 0;
    while(left <= right) {
        mid = left + Math.floor((right - left)/2);
        if (arr[mid] > v) {
            right = mid - 1;
        } else {
            left = mid + 1;
        }
    }

    return left;
}

/**
 * @param {number[]} chalk
 * @param {number} k
 * @return {number}
 */
function chalkReplacer (chalk, k) {
    const summation = _.sum(chalk);
    k = k % summation;
    const accumulation = chalk.reduce((acc, cur) => {
        if (acc.length > 0) {
            acc.push(acc[acc.length-1]+cur);
        } else {
            acc.push(cur);
        }
        return acc;
    }, []);
    let ans = binarySearch(accumulation, k);
    if (ans === -1) {
        ans = 0;
    }
    return ans;
}

